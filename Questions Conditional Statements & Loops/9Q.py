char = input("Enter character is a vowel or a consonant: ")
if char == 'a' or char == 'e' or char == 'i' or char == 'o' or char == 'u' or char == 'A' or char == 'E' or char == 'I' or char == 'O' or char == 'U':
    print("Vowel")
else:
    print("Consonant")